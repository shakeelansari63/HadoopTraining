var a = 20;
var b = 4;

// Sum
console.log(a + b);

// Difference
console.log(a - b);

// Product
console.log(a * b);

// Division
console.log(a / b);

// Short hand operator
// a = a + 5 ===> a += 5
var c;
c = 10;
c = c + 5;
console.log(c);

// Shorthand add
c = 10;
c += 5;
console.log(c);

// Shorthand minus
c = 10;
c -= 5;
console.log(c);

// Shorthand multiply
c = 10;
c *= 5;
console.log(c);

// Shorthand divide
c = 10;
c /= 5;
console.log(c);
