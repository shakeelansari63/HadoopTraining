// Create array
var pens1 = new Array("red", "black", "blue", "green");
console.log(pens1);

// Shorthand of creating array using []
var pens2;
pens2 = [ "red", "black", "blue", "green"];
console.log(pens2);

// Array can mix data types
var data = ["red", 3, "worker", true];
console.log(data);

// Arrays are indexed from 0
console.log(data[0]);
console.log(pens1[2]);
